    private void showData(){
        try {
            //clear bảng
            tMark.removeAll();
            String id = txtIDS.getText();
            String[] arr = {"SubjectID","Mark factor 1","Mark factor 2","Mark factor 3"};
            //Mô hình bảng của JLabel truy xuất = vector
            DefaultTableModel model = new DefaultTableModel(arr, 0);  
            //Lấy data của class khi connect mở <hàm connect viết bên kia>
            if(Connect.open()){
                ps = Connect.cnn.prepareStatement("Select subjects.subject_name, mark1, mark2, mark3 from mark,subjects where subjects.subject_id=mark.subject_id AND student_id="+id);            
            rs = ps.executeQuery();
                while (rs.next()) {
                    //Biến vector hoạt động khá giống 1 biến mảng
                    Vector vector = new Vector();
                    vector.add(rs.getString("subject_name"));
                    //vector.add(rs.getString("student_id"));
                    vector.add(rs.getString("mark1"));
                    vector.add(rs.getString("mark2"));
                    vector.add(rs.getString("mark3"));
                    //vector.add(rs.getString("medium_mark"));
                    //Chạy hàm addRow
                    model.addRow(vector);
                }
            }
            //đóng kết nối
            Connect.close(ps, rs);
            //Gắn vô bảng
            tMark.setModel(model);
        } catch (SQLException ex) {
            System.out.println("Not connect");
        }
    }
    
    private boolean check_id(){
        if(Connect.open()){
            try {
                //Chạy câu query dấu ? đại diện cho từng cái id trong table, chạy mà nó thấy thì nó trả false
                ps = Connect.cnn.prepareStatement("Select * from students where student_id = ?");
                ps.setString(1, txtIDS.getText());
                rs = ps.executeQuery();
                while (rs.next()) {
                    return false;                    
                }
            } catch (SQLException ex) {
                Logger.getLogger(formStudent.class.getName()).log(Level.SEVERE, null, ex);
            }
        }      
        return true;
    }    
