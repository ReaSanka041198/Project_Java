    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        check_input();
        if(check_id()==false){
            JOptionPane.showMessageDialog(this, "subject exist");
            txtID.requestFocus();
            return;
        }else{
            if(Connect.open()){
                try {
                    //Mấy dấu ? đại diện cho 1 bột
                    ps = Connect.cnn.prepareStatement("INSERT INTO subjects (subject_id, subject_name)"+"VALUES(?,?)");
                    ps.setString(1, txtID.getText());
                    ps.setString(2, txtName .getText());
                    ps.executeUpdate();  
                    //cập nhật lại db
                    showData();
                    JOptionPane.showMessageDialog(this, "Add success");
                    Connect.close(ps, rs);                   
                } catch (SQLException ex) {
                    System.out.println("Add faild, please check again");
                }
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

