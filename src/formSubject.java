    private void showData(){
        try {
            //clear bảng
            tSubject.removeAll();
            String[] arr = {"SubjectID","SubjectName"};
            //Mô hình bảng của JLabel truy xuất = vector
            DefaultTableModel model = new DefaultTableModel(arr, 0);  
            //Lấy data của class khi connect mở <hàm connect viết bên kia>
            if(Connect.open()){
                ps = Connect.cnn.prepareStatement("Select * from subjects");            
            rs = ps.executeQuery();
                while (rs.next()) {
                    //Biến vector hoạt động khá giống 1 biến mảng
                    Vector vector = new Vector();
                    vector.add(rs.getString("subject_id"));
                    vector.add(rs.getString("subject_name"));
                    //Chạy hàm addRow
                    model.addRow(vector);
                }
            }
            //đóng kết nối
            Connect.close(ps, rs);
            //Gắn vô bảng
            tSubject.setModel(model);
        } catch (SQLException ex) {
            System.out.println("Not connect");
        }
    }
    
    public void check_input(){
        if(txtID.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please import id subject");
            txtID.requestFocus();
            return;
        }                
        else if(txtName.getText().equals("")){
            JOptionPane.showMessageDialog(this, "please import name subject");
            txtName.requestFocus();
            return;
        }        
    }
    
    private boolean check_id(){
        if(Connect.open()){
            try {
                //Chạy câu query dấu ? đại diện cho từng cái id trong table, chạy mà nó thấy thì nó trả false
                ps = Connect.cnn.prepareStatement("Select * from subjects where subject_id = ?");
                ps.setString(1, txtID.getText());
                rs = ps.executeQuery();
                while (rs.next()) {
                    return false;                    
                }
            } catch (SQLException ex) {
                Logger.getLogger(formStudent.class.getName()).log(Level.SEVERE, null, ex);
            }
        }      
        return true;
    }
    
